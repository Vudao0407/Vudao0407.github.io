let showProduct = $("#showMenu");

arrPoduct.forEach((item) => {
  showProduct.append(`
  <div class="menu-content__box-item" class="item-pro" onclick="goToDetail(this)" data-id="${item.id}">
        <div class="imgBox">
            <div><img class="lazy img-fluid" src="${item.img}"></div>
        </div>
        <div class="textBox">
            <h3>${item.name}</h3>
        </div>
     </div>
 `);
});

const goToDetail = (dom) => {
  console.log(dom);
  let id = dom.dataset.id;
  let index = arrPoduct.findIndex((item) => item.id == id);
  let item = arrPoduct[index];
  localStorage.setItem("data-product-detail", JSON.stringify(item));
  // redirect sang trang chi tiet
  location.replace("./product-detail.html");
};
